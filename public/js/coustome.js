function toggleAccordion(heading) {
  if (window.innerWidth <= 768) { // Only for mobile/tablet
    const allHeadings = document.querySelectorAll(".foot_head");
    const allContents = document.querySelectorAll(".footer_directory");

    // Close all accordions first
    allHeadings.forEach(h => h.classList.remove("active"));
    allContents.forEach(c => c.classList.remove("active"));

    // Open the clicked one
    heading.classList.add("active");
    heading.nextElementSibling.classList.add("active");
  }
}



function copyText(button) {
  let textBox = button.previousElementSibling;
  navigator.clipboard.writeText(textBox.value)
      .then(() => {
          let message = button.nextElementSibling;
          message.innerText = "Text Copied !";
      })
      .catch(err => {
          let message = button.nextElementSibling;
          message.innerText = "Failed to copy text.";
      });
}



